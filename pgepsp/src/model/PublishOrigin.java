package model;

public class PublishOrigin {
	private int id;
	private String po_name;
	private String po_describe;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPo_name() {
		return po_name;
	}
	public void setPo_name(String po_name) {
		this.po_name = po_name;
	}
	public String getPo_describe() {
		return po_describe;
	}
	public void setPo_describe(String po_describe) {
		this.po_describe = po_describe;
	}
}
